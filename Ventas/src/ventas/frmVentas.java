    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        wcant = (int) jSpinner1.getValue();
        if ((wnomart == null) || (wnomart.isEmpty())) {
            JOptionPane.showMessageDialog(null,
                    "Elija Un Producto Producto");
            jTextField2.requestFocus();
            return;
        }
        if (wcant <= 0) {
            JOptionPane.showMessageDialog(null,
                    "Elija Una Cantidad");
            jSpinner1.requestFocus();
            return;
        }
        //Sección 1 
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        //Sección 2 
        Object[] fila = new Object[6];

        //Sección 3
        fila[0] = wcodart;
        fila[1] = wnomart;
        fila[2] = wprecio;
        fila[3] = wcant;
        fila[4] = wporiva;
        Double wporiv = (Double.valueOf(wporiva) / 100) + 1;
        fila[5] = wcant * wprecio * wporiv;

        //Sección 4
        modelo.addRow(fila);

        //Sección 5
        jTable1.setModel(modelo);
        double t = 0;
        double p;
        double i;
        double b;
        double vb = 0;

        if (jTable1.getRowCount() > 0) {
            for (int j = 0; j < jTable1.getRowCount(); j++) {
                p = Double.parseDouble(jTable1.getValueAt(j, 5).toString());
                t += p;
                b = Double.parseDouble(jTable1.getValueAt(j, 2).toString()) * Double.parseDouble(jTable1.getValueAt(j, 3).toString());
                vb += b;
            }
            i = t - vb;
            jTextField4.setText(String.valueOf(i));
            jTextField5.setText(String.valueOf(vb));
            jTextField3.setText(String.valueOf(t));
        }


    }//GEN-LAST:event_jButton1ActionPerformed

