    private void jTextField2FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField2FocusLost
        // TODO add your handling code here:

        try {
            ConexionMySQL SQL = new ConexionMySQL();
// Llamas al método que tiene la clase y te devuelve una conexión
            Connection conne = SQL.conectarMySQL();
// Query que usarás para hacer lo que necesites
            String wcod = jTextField2.getText();
            String sSQL = "select * from productos where codigo='" + wcod + "'";

            Statement statement = conne.createStatement();
            ResultSet rs = statement.executeQuery(sSQL);
            int nr = rs.getMetaData().getColumnCount();
            if (nr <= 0) {
                JOptionPane.showMessageDialog(null, "No Se Encuentra El Codigo");

                //sSQL = "select * from productos";
            }

            if (rs.next() == true) {

                wcodart = rs.getString("codigo");
                wnomart = rs.getString("nombre");
                wprecio = rs.getDouble("precio");
                wporiva = rs.getInt("poriva");
                wstockmin = rs.getInt("stockmin");
                jLabel4.setText(wnomart + ", Precio: " + wprecio);

                //System.out.println(String.format("%s, %s %s, %s", wcodart, wnomart, wprecio, wporiva));
            }

            rs.close();
            statement.close();
            conne.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "SQLException:\n" + e, "Error: Consultar(Productos)", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTextField2FocusLost

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jFormattedTextField2.getText() == null || jFormattedTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Elija Una Fecha");
            jFormattedTextField2.requestFocus();
            return;
        }
        if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "No Hay Datos En La DataTable");
            return;
        }
        jButton3.setEnabled(false);

        try {
            ConexionMySQL SQL = new ConexionMySQL();
// Llamas al método que tiene la clase y te devuelve una conexión
            Connection conne = SQL.conectarMySQL();
            String tSQL = "Select consfact+1 consfact from consecutivos";
            Statement statement = conne.createStatement();
            ResultSet rs = statement.executeQuery(tSQL);
            String mnumfact = "";
            while (rs.next()) {
                mnumfact = rs.getString("consfact");
            }
            tSQL = "update consecutivos set consfact=consfact+1 ";
            PreparedStatement pstmt = conne.prepareStatement(tSQL);
            pstmt.executeUpdate();
            pstmt.close();
// Query que usarás para hacer lo que necesites
            String sSQL;
            String wnumfact = mnumfact;
            Double wvalor = Double.parseDouble(jTextField3.getText());
            Double wiva = Double.parseDouble(jTextField4.getText());
            String wcliente = "A1";
            String wfecha = jFormattedTextField2.getText();

            sSQL = "insert into ventas_encabezado (numfac,valor,iva,cliente,fecha,fecsys)"
                    + " Values (?,?,?,?,?,now())";

            PreparedStatement pstm = conne.prepareStatement(sSQL);
            pstm.setString(1, wnumfact);
            pstm.setDouble(2, wvalor);
            pstm.setDouble(3, wiva);
            pstm.setString(4, wcliente);
            pstm.setString(5, wfecha);
            pstm.executeUpdate();
            pstm.close();
            String uSQL;
             
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                    
                uSQL = "INSERT INTO ventas_detalle VALUES(" + wnumfact + "," + jTable1.getValueAt(i, 0) + ","
                        + jTable1.getValueAt(i, 3) + "," + jTable1.getValueAt(i, 2) + "," + jTable1.getValueAt(i, 4) + ")";
               PreparedStatement pstmu = conne.prepareStatement(uSQL);
                pstmu.executeUpdate();
                pstmu.close();
            }
            
            
            conne.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "SQLException:\n" + e, "Error: Insertar(Ventas_Encabezado)", JOptionPane.ERROR_MESSAGE);

        }

        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField2.setText("");
        jLabel4.setText(".");
        jSpinner1.setValue(0);
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
        JOptionPane.showMessageDialog(null, "Factura Grabada Con Exito");
        jButton3.setEnabled(true);


    }//GEN-LAST:event_jButton3ActionPerformed

